def parse_arabic_grade_list(grades_text: str) -> set:
    """
    Parse Arabic comma-separated grade names into canonical grade strings.
    Example:
    'الخامس, السادس, السابع' -> {'5', '6', '7'}
    """
    if pd.isna(grades_text):
        return set()

    parts = [p.strip() for p in str(grades_text).split(",")]
    normalized = set()

    for part in parts:
        mapped = normalize_grade(part)
        normalized.add(str(mapped))

    return normalized

