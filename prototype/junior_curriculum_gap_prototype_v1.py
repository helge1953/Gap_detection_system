def build_direct_prereq_links_for_targets(target_topics: pd.DataFrame) -> pd.DataFrame:
    """
    For each target topic, find its direct prerequisites from prereq_df
    and enrich them with readable topic names from topics_df.
    """
    # Keep only the prerequisite edges that point to the selected target topics
    target_ids = set(target_topics["topic_id"].tolist())
    edges = prereq_df[prereq_df["to_topic_id"].isin(target_ids)].copy()

    if edges.empty:
        return pd.DataFrame()

    # Merge target topic info
    target_info = topics_df[["topic_id", "topic_name_en", "grade", "stream", "subject_branch"]].copy()
    target_info = target_info.rename(columns={
        "topic_id": "to_topic_id",
        "topic_name_en": "target_topic_name_en",
        "grade": "target_grade",
        "stream": "target_stream",
        "subject_branch": "target_subject_branch"
    })

    # Merge prerequisite topic info
    prereq_info = topics_df[["topic_id", "topic_name_en", "grade", "stream", "subject_branch"]].copy()
    prereq_info = prereq_info.rename(columns={
        "topic_id": "from_topic_id",
        "topic_name_en": "prereq_topic_name_en",
        "grade": "prereq_grade",
        "stream": "prereq_stream",
        "subject_branch": "prereq_subject_branch"
    })

    enriched = edges.merge(target_info, on="to_topic_id", how="left")
    enriched = enriched.merge(prereq_info, on="from_topic_id", how="left")

    return enriched.sort_values(
        by=["to_topic_id", "from_topic_id"]
    ).reset_index(drop=True)

