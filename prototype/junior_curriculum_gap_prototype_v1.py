def build_curriculum_graph(
    country: str,
    grade: str,
    stream: str,
    subject: str = "Math"
):
    """
    Build a graph-ready curriculum slice for a specific country/grade/stream/subject.
    """
    slice_topics = topics_df[
        (topics_df["country"] == country) &
        (topics_df["grade"] == str(grade)) &
        (topics_df["stream"] == stream) &
        (topics_df["subject"] == subject) &
        (topics_df["is_core_topic"] == 1)
    ].copy()

    topic_ids = set(slice_topics["topic_id"].tolist())

    slice_edges = prereq_df[
        prereq_df["from_topic_id"].isin(topic_ids) &
        prereq_df["to_topic_id"].isin(topic_ids)
    ].copy()

    nodes_df = slice_topics[["topic_id", "topic_name_en", "subject_branch"]].copy()
    nodes_df = nodes_df.rename(columns={
        "topic_id": "node_id",
        "topic_name_en": "node_label",
        "subject_branch": "node_group"
    })
    nodes_df["node_type"] = "curriculum_topic"
    nodes_df["node_status"] = "curriculum"

    edges_df = slice_edges[[
        "from_topic_id",
        "to_topic_id",
        "relation_type",
        "strength"
    ]].copy()

    edges_df = edges_df.rename(columns={
        "from_topic_id": "source",
        "to_topic_id": "target",
        "relation_type": "edge_type"
    })

    return nodes_df.reset_index(drop=True), edges_df.reset_index(drop=True)

