def build_candidate_support_topics(direct_links: pd.DataFrame) -> pd.DataFrame:
    """
    Aggregate prerequisite topics into a candidate support-topic list.
    Each row represents one prerequisite topic and summarizes how it supports
    the current target curriculum slice.
    """
    if direct_links.empty:
        return pd.DataFrame()

    grouped = (
        direct_links.groupby(
            [
                "from_topic_id",
                "prereq_topic_name_en",
                "prereq_grade",
                "prereq_stream",
                "prereq_subject_branch",
            ],
            dropna=False
        )
        .agg(
            support_count=("to_topic_id", "nunique"),
            relation_types=("relation_type", lambda x: ", ".join(sorted(set(x)))),
            strengths=("strength", lambda x: ", ".join(sorted(set(x)))),
            cross_grade_edges=("is_cross_grade", "sum"),
            cross_country_edges=("is_cross_country", "sum"),
            supported_targets=("target_topic_name_en", lambda x: " | ".join(sorted(set(x))))
        )
        .reset_index()
    )

    grouped = grouped.sort_values(
        by=["support_count", "cross_grade_edges", "cross_country_edges", "from_topic_id"],
        ascending=[False, False, False, True]
    ).reset_index(drop=True)

    return grouped

