def build_student_graph_nodes(
    target_topics_df: pd.DataFrame,
    candidate_support_topics_df: pd.DataFrame
) -> pd.DataFrame:
    """
    Build graph nodes for a student-specific knowledge graph.
    """
    target_nodes = target_topics_df[[
        "topic_id",
        "topic_name_en"
    ]].copy()

    target_nodes = target_nodes.rename(columns={
        "topic_id": "node_id",
        "topic_name_en": "node_label"
    })
    target_nodes["node_type"] = "target_topic"
    target_nodes["node_status"] = "target"

    prereq_nodes = candidate_support_topics_df[[
        "from_topic_id",
        "prereq_topic_name_en",
        "coverage_status"
    ]].copy()

    prereq_nodes = prereq_nodes.rename(columns={
        "from_topic_id": "node_id",
        "prereq_topic_name_en": "node_label",
        "coverage_status": "node_status"
    })
    prereq_nodes["node_type"] = "prereq_topic"

    nodes_df = pd.concat([target_nodes, prereq_nodes], ignore_index=True)
    nodes_df = nodes_df.drop_duplicates(subset=["node_id"]).reset_index(drop=True)

    return nodes_df

