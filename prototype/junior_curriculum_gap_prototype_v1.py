def build_target_prereq_report(direct_links: pd.DataFrame, candidate_support_topics: pd.DataFrame) -> pd.DataFrame:
    """
    Build a target-topic-centered report:
    each row = one target topic + one prerequisite topic + coverage status
    """
    if direct_links.empty:
        return pd.DataFrame()

    # Bring in coverage + support metadata from candidate support topics
    support_lookup = candidate_support_topics[[
        "from_topic_id",
        "coverage_status",
        "prereq_country",
        "support_count",
        "cross_grade_edges",
        "cross_country_edges",
        "relation_types",
        "strengths"
    ]].drop_duplicates(subset=["from_topic_id"])

    report_df = direct_links.merge(
        support_lookup,
        on="from_topic_id",
        how="left",
        suffixes=("", "_agg")
    )

    report_df = report_df[[
        "to_topic_id",
        "target_topic_name_en",
        "from_topic_id",
        "prereq_topic_name_en",
        "prereq_country",
        "prereq_grade",
        "prereq_stream",
        "prereq_subject_branch",
        "relation_type",
        "strength",
        "is_cross_grade",
        "is_cross_country",
        "coverage_status",
        "support_count"
    ]].copy()

    report_df = report_df.sort_values(
        by=["to_topic_id", "coverage_status", "from_topic_id"]
    ).reset_index(drop=True)

    return report_df

